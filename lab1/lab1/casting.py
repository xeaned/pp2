#integers

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

#floats
q = float(1)     # q will be 1.0
w = float(2.8)   # w will be 2.8
e = float("3")   # e will be 3.0
r = float("4.2") # r will be 4.2

#strings
t = str("s1") # t will be 's1'
g = str(2)    # g will be '2'
l = str(3.0)  # l will be '3.0'
